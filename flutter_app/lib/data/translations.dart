/// Helper: get a translation string for the given language and key.
/// Falls back to English, then to the key itself.
String tr(String lang, String key) {
  final langMap = kTranslations[lang] ?? kTranslations['en']!;
  final value = langMap[key];
  if (value is String) return value;
  final enMap = kTranslations['en']!;
  final enValue = enMap[key];
  if (enValue is String) return enValue;
  return key;
}

/// Get color name for a given hex string in the current language.
String colorName(String lang, String hex) {
  final langMap = kTranslations[lang] ?? kTranslations['en']!;
  final colors = langMap['colors'];
  if (colors is Map) {
    final name = colors[hex];
    if (name is String) return name;
  }
  return hex;
}
